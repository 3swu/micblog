package com.bean;

public class Item {
	private int item_id;
	private int item_userid;
	private String item_time;
	private String item_str;
	private int item_star;
	private String item_starIdStr;
	public int getItem_star() {
		return item_star;
	}
	public void setItem_star(int item_star) {
		this.item_star = item_star;
	}
	public String getItem_starIdStr() {
		return item_starIdStr;
	}
	public void setItem_starIdStr(String item_starIdStr) {
		this.item_starIdStr = item_starIdStr;
	}
	public int getItem_id() {
		return item_id;
	}
	public void setItem_id(int item_id) {
		this.item_id = item_id;
	}
	public int getItem_userid() {
		return item_userid;
	}
	public void setItem_userid(int item_userid) {
		this.item_userid = item_userid;
	}
	public String getItem_time() {
		return item_time;
	}
	public void setItem_time(String item_time) {
		this.item_time = item_time;
	}
	public String getItem_str() {
		return item_str;
	}
	public void setItem_str(String item_str) {
		this.item_str = item_str;
	}
	
}
